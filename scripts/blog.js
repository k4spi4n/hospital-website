function getPostSlug() {
  const params = new URLSearchParams(window.location.search);
  return params.get("post") || "";
}

function renderArticle(post) {
  const titleElement = document.getElementById("blogTitle");
  const excerptElement = document.getElementById("blogExcerpt");
  const dateElement = document.getElementById("blogDate");
  const categoryElement = document.getElementById("blogCategory");
  const coverElement = document.getElementById("blogCover");
  const contentElement = document.getElementById("blogContent");

  if (
    !titleElement ||
    !excerptElement ||
    !dateElement ||
    !categoryElement ||
    !coverElement ||
    !contentElement
  ) {
    return;
  }

  titleElement.textContent = post.title;
  excerptElement.textContent = post.excerpt;
  dateElement.textContent = post.date;
  categoryElement.textContent = post.category;

  coverElement.src = post.image;
  coverElement.alt = post.imageAlt;

  const sectionHtml = post.sections
    .map(
      (section) => `
        <section>
          <h2>${section.heading}</h2>
          ${section.paragraphs.map((text) => `<p>${text}</p>`).join("")}
        </section>
      `,
    )
    .join("");

  contentElement.innerHTML = sectionHtml;
  document.title = `${post.title} | CMEC Insight`;
}

function renderRelatedPosts(currentSlug) {
  const relatedElement = document.getElementById("relatedPosts");

  if (!relatedElement) {
    return;
  }

  const items = Object.entries(posts)
    .filter(([slug]) => slug !== currentSlug)
    .map(
      ([slug, post]) => `
        <li>
          <a href="./blog.html?post=${slug}">
            <strong>${post.title}</strong>
            <span>${post.date}</span>
          </a>
        </li>
      `,
    )
    .join("");

  relatedElement.innerHTML = items;
}

function renderNotFound() {
  const fallbackPost = {
    title: "Bài viết không tồn tại",
    excerpt:
      "Nội dung bạn tìm kiếm hiện không có sẵn. Vui lòng quay lại trang Tin tức để chọn bài viết khác.",
    date: "CMEC Insight",
    category: "Thông báo",
    image: "./assets/trangchu.jpg",
    imageAlt: "Không tìm thấy bài viết",
    sections: [
      {
        heading: "Gợi ý",
        paragraphs: [
          "Hãy quay lại mục Tin tức & Sự kiện y tế để xem các bản tin mới nhất từ CMEC Hospital.",
        ],
      },
    ],
  };

  renderArticle(fallbackPost);

  const relatedElement = document.getElementById("relatedPosts");
  if (relatedElement) {
    relatedElement.innerHTML =
      '<li><a href="./index.html#news"><strong>Quay lại trang Tin tức</strong><span>Xem toàn bộ bài viết</span></a></li>';
  }
}

function initBlogPage() {
  const slug = getPostSlug();
  const post = posts[slug];

  if (!post) {
    renderNotFound();
    return;
  }

  renderArticle(post);
  renderRelatedPosts(slug);
}

initBlogPage();
