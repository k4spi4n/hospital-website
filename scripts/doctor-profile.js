function getDoctorSlug() {
  const params = new URLSearchParams(window.location.search);
  return params.get("doctor") || "";
}

function setText(id, value) {
  const element = document.getElementById(id);
  if (element) {
    element.textContent = value;
  }
}

function renderList(id, items) {
  const list = document.getElementById(id);
  if (!list) {
    return;
  }

  list.innerHTML = items.map((item) => `<li>${item}</li>`).join("");
}

function renderDoctor(profile) {
  setText("doctorName", profile.name);
  setText("doctorTitle", profile.title);
  setText("doctorIntroLead", profile.introLead);
  setText("doctorOverview", profile.overview);
  setText("doctorDepartment", profile.department);
  setText("doctorDegree", profile.degree);
  setText("doctorYears", profile.years);
  setText("doctorSchedule", profile.schedule);
  setText("doctorExperience", profile.experience);

  renderList("doctorTimeline", profile.timeline);
  renderList("doctorStrengths", profile.strengths);

  const avatar = document.getElementById("doctorAvatar");
  if (avatar) {
    avatar.src = profile.image;
    avatar.alt = profile.imageAlt;
  }

  document.title = `${profile.name} | Hồ sơ bác sĩ CMEC`;
}

